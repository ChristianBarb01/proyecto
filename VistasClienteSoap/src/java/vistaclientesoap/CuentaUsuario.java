        void CargarDatos(String NumCuenta) {
        for (int i = 1; i <= CuentaBanc.us - 1; i++) {
            if (NumCuenta.equals(CuentaBanc.user[i].Usuario)) {
                this.txtUs.setText(CuentaBanc.user[i].Usuario);
                this.txtCuenta.setText(CuentaBanc.user[i].NCuenta);
                this.txtSaldo.setText(String.valueOf(CuentaBanc.user[i].Saldo));
                break;
            }
        }
    }

    

