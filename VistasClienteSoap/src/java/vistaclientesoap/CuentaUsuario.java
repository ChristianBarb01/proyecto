    private void BtnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnIngresarActionPerformed
   if (RDeposito.isSelected()) {
            CuentaBanc.Depositar(cuentaP, Double.parseDouble(this.txtValor.getText()));
            CargarDatos(cuentaP);
            JOptionPane.showMessageDialog(null, "Deposito realizado con exito");
            this.txtValor.setText("");
        } else if (RRetiro.isSelected()) {

            if (Double.parseDouble(txtSaldo.getText()) >= Double.parseDouble(txtValor.getText())) {
                CuentaBanc.Retirar(cuentaP, Double.parseDouble(this.txtValor.getText()));
                CargarDatos(cuentaP);
                 JOptionPane.showMessageDialog(null, "Retiro realizado con exito");
                this.txtValor.setText("");;
            } else {
                 JOptionPane.showMessageDialog(null, "Saldo insuficiente");

            }

        }
    }//GEN-LAST:event_BtnIngresarActionPerformed

